			<p>Sistem Informasi Geografis (SIG) atau Geographic Information System (GIS) adalah sistem informasi pemetaan berbasis komputer yang digunakan untuk memasukkan, menyimpan, memanggil kembali, mengolah, menganalisis dan menghasilkan data bereferensi geografis atau data geospatial, untuk mendukung pengambilan keputusan dalam perencanaan dan pengelolaan penggunaan lahan, sumber daya alam, lingkungan, transportasi, fasilitas kota, dan pelayanan umum lainnya. Teknologi Sistem Informasi Geografis juga dapat digunakan untuk investigasi ilmiah, pengelolaan sumber daya, perencanaan pembangunan, kartografi dan perencanaan rute. Hasil akhir dari proses GIS diwujudkan dalam peta atau grafik. Peta sangatlah efektif untuk menyimpan, memvisualisasikan dan memberikan informasi geografis. Program pelatihan ini terdiri dari dua level. Fundamental GIS mempelajari GIS berbasis desktop mengunakan ArcView, dan Advanced GIS mempelajari GIS berbasis web menggunakan MapServer.</p>
